// Function to get random image from category
export const getRandomImage = (category) => {
    const images = copyrightFreeImages[category] || copyrightFreeImages.gallery;
    return images[Math.floor(Math.random() * images.length)];
};

// Function to get specific image by index
export const getImageByIndex = (category, index) => {
    const images = copyrightFreeImages[category] || copyrightFreeImages.gallery;
    return images[index % images.length];
};

// Function to get all images from a category
export const getAllImages = (category) => {
    return copyrightFreeImages[category] || copyrightFreeImages.gallery;
};
